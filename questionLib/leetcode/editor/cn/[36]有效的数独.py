from collections import Counter


class Solution:
    def isValid(self, flatList):
        cnt = Counter("".join(flatList))
        for k, v in cnt.items():
            if k != '.' and v > 1:
                return False
        return True

    def isValidSudoku(self, board) -> bool:
        border = [[0, 3], [3, 6], [6, 9]]
        # 1.éå†æ¯ä¸€è¡Œ
        for i in range(9):
            if not self.isValid(board[i]):
                return False
        # 2. éå†æ¯ä¸€åˆ—, æ³¨æ„ä¸èƒ½é‡‡å–ç±»ä¼¼numpyçš„ç´¢å¼•æ–¹å¼board[:][j]
        for j in range(9):
            tmp = [board[i][j] for i in range(9)]
            if not self.isValid(tmp):
                return False
        # 3. éå†æ¯ä¸€å®«
        for i in border:
            for j in border:
                Gong = board[i[0]:i[1]]
                flatGong = [Gong[m][n] for m in range(3) for n in range(j[0], j[1])]
                # print(flatGong)
                if not self.isValid(flatGong):
                    return False
        return True

