    def sumRootToLeaf(self, root: Optional[TreeNode]) -> int:
        def dfs(root, val):
            if not root:
                return 0
            val = (val << 1) | root.val
            if not root.left and not root.right:
                return val
            return dfs(root.left, val) + dfs(root.right, val)

        return dfs(root, 0)

