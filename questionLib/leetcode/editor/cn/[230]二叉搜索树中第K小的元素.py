# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def __init__(self):
        self.ans = -1
        self.cnt = 0

    def kthSmallest(self, root: Optional[TreeNode], k: int) -> int:
        if not root:
            return self.ans
        self.kthSmallest(root.left, k)
        self.cnt += 1
        if self.cnt == k:
            self.ans = root.val
        self.kthSmallest(root.right, k)
        return self.ans

